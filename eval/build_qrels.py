pool_path = Path("pool_for_annotation.tsv")
qrels_path = Path("qrels.tsv")

seen = set()
rows = []
with open(pool_path, encoding="utf-8") as f:
    for line in f:
        parts = line.rstrip("\n").split("\t")
        if len(parts) < 2:
            continue
        qid, doc_id = parts[0], parts[1]
        key = (qid, doc_id)
        if key in seen:
            continue
        seen.add(key)
        rel = 1 if doc_id in RELEVANT.get(qid, set()) else 0
        rows.append((qid, doc_id, rel))

with open(qrels_path, "w", encoding="utf-8") as f:
    for qid, doc_id, rel in rows:
        f.write(f"{qid}\t0\t{doc_id}\t{rel}\n")

