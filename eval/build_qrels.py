print(f"qrels.tsv gerado: {len(rows)} julgamentos, {n_rel} relevantes ({n_rel/len(rows):.1%}).")

from collections import Counter
per_q = Counter()
per_q_rel = Counter()
for qid, _, rel in rows:
    per_q[qid] += 1
    per_q_rel[qid] += rel
for qid in sorted(per_q, key=lambda x: int(x[1:])):
    print(f" {qid}: {per_q_rel[qid]}/{per_q[qid]} relevantes")
